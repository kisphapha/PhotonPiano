export default {
    name : "About",
    inject: ["eventBus"],

    mounted() {
      this.scrollToYPosition(0);
    },
    
    methods: {
      scrollToYPosition(targetY) {
        const scrollDuration = 500; // Duration of the scroll animation in milliseconds
        const scrollStep = (targetY - window.scrollY) / (scrollDuration / 15); // Scrolling distance per frame

        const scrollAnimation = setInterval(() => {
          if ((scrollStep > 0 && window.scrollY < targetY) || (scrollStep < 0 && window.scrollY > targetY)) {
            window.scrollBy(0, scrollStep);
          } else {
            clearInterval(scrollAnimation);
          }
        }, 15);
      },
      triggerOpenPopup() {
        this.eventBus.emit("open-login-popup")
      }
    }
  }
</script>

